    public void registerUser(String username, String password, String email) {
    String sql = "INSERT INTO users (username, password, email) VALUES (?, ?, ?)";

    try (Connection conn = DatabaseConnection.getConnection();
         PreparedStatement pstmt = conn.prepareStatement(sql)) {

        pstmt.setString(1, username);
        pstmt.setString(2, password);
        pstmt.setString(3, email);

        pstmt.executeUpdate();
        JOptionPane.showMessageDialog(null, "Registrasi berhasil!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

    } catch (SQLException e) {
        if (e.getMessage().contains("Duplicate entry")) {
            JOptionPane.showMessageDialog(null, "Username atau email sudah terdaftar!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Gagal registrasi: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
}
    
    private boolean validateRegistration(String username, String password, String email) {
    if (username.isEmpty() || password.isEmpty() || email.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Semua field harus diisi!", "Error", JOptionPane.WARNING_MESSAGE);
        return false;
    }

    if (!email.contains("@") || !email.contains(".")) {
        JOptionPane.showMessageDialog(null, "Format email tidak valid!", "Error", JOptionPane.WARNING_MESSAGE);
        return false;
    }

    if (password.length() < 6) {
        JOptionPane.showMessageDialog(null, "Password harus minimal 6 karakter!", "Error", JOptionPane.WARNING_MESSAGE);
        return false;
    }

    return true;
}

